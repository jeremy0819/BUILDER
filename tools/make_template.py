# -*- coding: utf-8 -*-
"""
產生「都更全案投報_對照範本.xlsx」
====================================
一張 `坪效及獲利分析` 分頁，欄位與公式 1:1 對齊 app.py 的 L6 都更全案投報層，
讓你打開舊的建築師 Excel 坪效分析表時，可逐格對照（D 欄貼舊值、E 欄自動算差異）。

執行：python make_template.py   →  產出 都更全案投報_對照範本.xlsx
公式忠於《都市更新權利變換實施辦法》費用負擔六大科目；數字基準＝合成案例（費率經真實案私下校準）。
"""
import openpyxl
from openpyxl.styles import Font, PatternFill, Alignment, Border, Side

黃 = PatternFill("solid", fgColor="FFF2CC")      # 輸入格
藍 = PatternFill("solid", fgColor="DDEBF7")      # 小計/重點
sec填 = PatternFill("solid", fgColor="538DD5")   # 區段標題
粗 = Font(bold=True)
細灰 = Side(style="thin", color="D9D9D9")
框 = Border(left=細灰, right=細灰, top=細灰, bottom=細灰)

C = {}  # 名稱 -> 儲存格位址（供公式參照）


def build():
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "坪效及獲利分析"
    for col, w in zip("ABCDE", (24, 15, 34, 14, 12)):
        ws.column_dimensions[col].width = w

    state = {"r": 1}

    def 列(名, 值, 單位="", *, 輸入=False, 重點=False, 對照=False, fmt=None):
        r = state["r"]
        a = ws.cell(r, 1, 名)
        b = ws.cell(r, 2, 值)
        ws.cell(r, 3, 單位)
        C[名] = f"B{r}"
        if 輸入:
            b.fill = 黃
            b.font = 粗
        if 重點:
            a.font = 粗
            b.font = 粗
            b.fill = 藍
        if fmt:
            b.number_format = fmt
        for col in range(1, 6):
            ws.cell(r, col).border = 框
        if 對照:                                   # D 欄貼舊表值、E 欄差異
            ws.cell(r, 4).fill = PatternFill("solid", fgColor="FFFFFF")
            e = ws.cell(r, 5, f'=IF(N(D{r})=0,"",B{r}-D{r})')
            if fmt:
                ws.cell(r, 4).number_format = fmt
                e.number_format = fmt
        state["r"] += 1

    def 區(t):
        r = state["r"]
        c = ws.cell(r, 1, t)
        c.font = Font(bold=True, color="FFFFFF")
        for col in range(1, 6):
            ws.cell(r, col).fill = sec填
        state["r"] += 1

    金 = "#,##0"
    pct = "0.0%"

    # ── 標題 ──
    t = ws.cell(1, 1, "都更全案投報 — 坪效及獲利分析（對照範本）")
    t.font = Font(bold=True, size=14)
    ws.merge_cells("A1:E1")
    note = ws.cell(2, 1, "黃底=輸入(只改這)｜白底=自動算｜D欄貼你舊Excel對應值｜E欄=差異(>±2%留意)｜範例＝合成案例C")
    note.font = Font(italic=True, size=9, color="808080")
    ws.merge_cells("A2:E2")
    ws.cell(3, 4, "舊表值").font = 粗
    ws.cell(3, 5, "差異").font = 粗
    state["r"] = 4

    # ── 輸入區 ──
    區("① 輸入區（黃底，每次換案只改這裡）")
    列("基地面積", 983.0, "使照㎡（非謄本）", 輸入=True)
    列("容積率", 2.25, "225%→2.25", 輸入=True)
    列("獎勵率", 0.88407, "都更50%+防災；88.4%→0.884", 輸入=True)
    列("容積移轉", 0.0, "㎡", 輸入=True)
    列("公設比", 0.33, "", 輸入=True, fmt=pct)
    列("外皮係數", 1.01, "", 輸入=True)
    列("銷售坪", 2108.48, "圖說面積表銷售坪", 輸入=True)
    列("總營建坪", 2550.44, "圖說總樓地板坪", 輸入=True)
    列("住宅單價", 130.0, "萬/坪", 輸入=True)
    列("店舖坪數", 66.0, "坪", 輸入=True)
    列("店舖單價", 182.0, "萬/坪", 輸入=True)
    列("車位數", 49, "位", 輸入=True)
    列("車位單價", 250.0, "萬/位", 輸入=True)
    列("營造單價", 24.0, "萬/坪（含工程費A）", 輸入=True)
    列("戶數", 40, "戶", 輸入=True)
    列("全案管理費率", 0.05, "總銷×", 輸入=True, fmt=pct)
    列("設計監造率", 0.05, "營造×", 輸入=True, fmt=pct)
    列("工程管理率", 0.03, "營造×", 輸入=True, fmt=pct)
    列("管維率", 0.01, "工程×", 輸入=True, fmt=pct)
    列("權變作業率", 0.015, "房地銷×（估價/規劃/鑑定）", 輸入=True, fmt=pct)
    列("拆補每戶", 90.0, "萬/戶", 輸入=True)
    列("月租金每戶", 2.0, "萬/戶·月", 輸入=True)
    列("安置月數", 42, "月", 輸入=True)
    列("營業稅率", 0.05, "房地銷×", 輸入=True, fmt=pct)
    列("印花稅率", 0.001, "總銷×", 輸入=True, fmt=pct)
    列("建融成數", 0.70, "", 輸入=True, fmt=pct)
    列("建融利率", 0.0325, "", 輸入=True, fmt=pct)
    列("建融年期", 3.0, "年", 輸入=True)
    列("現金流係數", 0.5, "", 輸入=True)

    # ── 坪效（L2）──
    區("② 坪效（容積帳；對照建築師面積表）")
    列("基準容積FA", f"={C['基地面積']}*{C['容積率']}", "基地×容積率", 對照=True, fmt=金)
    列("允建容積", f"={C['基準容積FA']}*(1+{C['獎勵率']})+{C['容積移轉']}", "FA×(1+獎勵)+容移",
       重點=True, 對照=True, fmt=金)
    列("允建容積坪", f"={C['允建容積']}/3.3058", "坪", 對照=True, fmt=金)

    # ── 總銷分析（L6-1）──
    區("③ 總銷分析")
    列("住宅坪數", f"={C['銷售坪']}-{C['店舖坪數']}", "銷售坪−店舖坪", fmt=金)
    列("住宅銷售", f"={C['住宅坪數']}*{C['住宅單價']}", "萬", 對照=True, fmt=金)
    列("店舖銷售", f"={C['店舖坪數']}*{C['店舖單價']}", "萬", fmt=金)
    列("房地總銷", f"={C['住宅銷售']}+{C['店舖銷售']}", "萬", fmt=金)
    列("車位銷售", f"={C['車位數']}*{C['車位單價']}", "萬", fmt=金)
    列("總銷", f"={C['房地總銷']}+{C['車位銷售']}", "萬", 重點=True, 對照=True, fmt=金)

    # ── 共同負擔六大科目（L6-2；都市更新權利變換實施辦法）──
    區("④ 共同負擔六大科目（權利變換實施辦法）")
    列("營造成本", f"={C['營造單價']}*{C['總營建坪']}", "營造單價×總營建坪", fmt=金)
    列("A 工程費用", f"={C['營造成本']}*(1+{C['設計監造率']}+{C['工程管理率']})",
       "營造+設計+工管", 對照=True, fmt=金)
    列("B 管維費用", f"={C['A 工程費用']}*{C['管維率']}", "容獎後續管維", fmt=金)
    列("C 權變費用",
       f"={C['房地總銷']}*{C['權變作業率']}+{C['戶數']}*{C['拆補每戶']}"
       f"+{C['戶數']}*{C['月租金每戶']}*{C['安置月數']}",
       "權變作業+拆補+租補", 對照=True, fmt=金)
    列("D 貸款利息",
       f"={C['A 工程費用']}*{C['建融成數']}*{C['建融利率']}*{C['建融年期']}*{C['現金流係數']}",
       "建融利息", fmt=金)
    列("E 稅捐", f"={C['房地總銷']}*{C['營業稅率']}+{C['總銷']}*{C['印花稅率']}",
       "營業稅+印花稅", fmt=金)
    列("F 管理費用", f"={C['總銷']}*{C['全案管理費率']}", "全案管理服務費", 對照=True, fmt=金)
    列("共同負擔", f"={C['A 工程費用']}+{C['B 管維費用']}+{C['C 權變費用']}"
                  f"+{C['D 貸款利息']}+{C['E 稅捐']}+{C['F 管理費用']}",
       "A+B+C+D+E+F", 重點=True, 對照=True, fmt=金)

    # ── 投報（L6-3）──
    區("⑤ 投報（分回 / 報酬率）")
    列("共負比", f"={C['共同負擔']}/{C['總銷']}", "共負÷總銷", 重點=True, 對照=True, fmt=pct)
    列("地主分回價值", f"={C['總銷']}-{C['共同負擔']}", "總銷−共負", 重點=True, 對照=True, fmt=金)
    列("地主分回比", f"={C['地主分回價值']}/{C['總銷']}", "", 重點=True, 對照=True, fmt=pct)
    列("報酬率", f"={C['地主分回價值']}/{C['共同負擔']}", "利潤÷總成本", 重點=True, 對照=True, fmt=pct)
    列("每戶分回(示意)", f"={C['地主分回價值']}/{C['戶數']}", "萬/戶（依權值估價）", fmt=金)

    # ── 來源頁 ──
    s2 = wb.create_sheet("參數來源與說明")
    s2.column_dimensions["A"].width = 22
    s2.column_dimensions["B"].width = 70
    源 = [
        ("項目", "來源 / 說明"),
        ("共同負擔六科目", "都市更新權利變換實施辦法（費用負擔：工程/權變/利息/稅捐/管理/其他）"),
        ("都更容積獎勵上限", "都市更新條例 §65：基準容積 1.5 倍（+50%）"),
        ("台北市防災型都更", "都更獎勵之上再加最高 30%（ID值<0.35、基地≥1000㎡）"),
        ("營造單價/設計費/稅", "真實案估價師送審試算（私有校準）：營造22萬·營業稅5%·印花0.1%"),
        ("住宅單價(合成案例C)", "依區位行情帶入；合成示範值 136萬/坪"),
        ("公式對齊", "本表公式與 app.py 之 calc_投報全案 一致；數字應相同。"),
        ("用法", "黃底改成你的案；D欄貼舊Excel對應值，E欄自動顯示差異。"),
        ("免責", "前期評估參考，非正式財務/法律意見；以估價師核定與最新法令為準。"),
    ]
    for i, (a, b) in enumerate(源, 1):
        s2.cell(i, 1, a).font = 粗 if i == 1 else Font()
        s2.cell(i, 2, b)

    wb.save("都更全案投報_對照範本.xlsx")
    print("已產出 都更全案投報_對照範本.xlsx")


if __name__ == "__main__":
    build()
