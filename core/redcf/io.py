import pandas as pd


def 解析上傳(file) -> pd.DataFrame:
    """讀 Excel/CSV，盡量對應到標準欄位。對不上的欄位留空，讓使用者在表中補。

    別名表涵蓋台灣建築師事務所常見欄位命名（建照申請、使照圖說格式）。
    """
    if file.name.lower().endswith(".csv"):
        df = pd.read_csv(file)
    else:
        df = pd.read_excel(file)
    別名 = {
        "樓層": ["樓層", "層", "floor", "Floor", "樓別", "FL", "F.", "層別", "棟層", "樓",
                "FLOOR", "Level"],
        "樓板": ["樓板", "樓地板", "樓板面積", "樓地板面積", "樓板面積(m²)", "樓地板面積(m²)",
                "總面積", "A面積", "各層樓地板面積", "樓地板"],
        "計容積": ["計容積", "計入容積", "容積面積", "樓地板面積(容積)", "容積計算面積",
                  "容積樓地板面積", "計容面積", "容積 m²", "容積計入", "計入容積(m²)",
                  "計入容積面積", "容積計算"],
        "梯廳": ["梯廳", "梯廳面積", "走道", "電梯廳", "梯廳及電梯", "梯廳(m²)",
                "梯廳走道", "走廊", "走廊梯廳", "電梯間及梯廳"],
        "安全梯": ["安全梯", "安全梯機電", "安全梯及機電", "機電", "安全梯(m²)",
                  "樓梯間", "安全梯間", "安全梯機電空間", "防火梯", "特別安全梯"],
        "陽台": ["陽台", "陽臺", "陽台面積", "露台", "陽台(m²)", "陽台及露台",
                "陽台面積(m²)", "陽臺面積", "露臺"],
    }
    out = pd.DataFrame()
    out["啟用"] = True
    cols = {str(c).strip(): c for c in df.columns}
    for 標準, 候選 in 別名.items():
        命中 = next((cols[a] for a in 候選 if a in cols), None)
        out[標準] = df[命中] if 命中 else (range(1, len(df) + 1) if 標準 == "樓層" else 0.0)
    out = out[["啟用", "樓層", "樓板", "計容積", "梯廳", "安全梯", "陽台"]]
    for c in ["樓板", "計容積", "梯廳", "安全梯", "陽台"]:
        out[c] = pd.to_numeric(out[c], errors="coerce").fillna(0.0)
    out["啟用"] = True
    out["樓層"] = out["樓層"].astype(str)
    if len(out) == 0:
        raise ValueError("檔案沒有任何資料列")
    return out


