# -*- coding: utf-8 -*-
"""
core/capacity.py — 容積計算層（L2–L4）
=====================================================
Urban Renewal Core Engine ── 建築容積查核 + 容積獎勵驗核。
法源：建築技術規則 §162（三項免計，逐層）、都市更新建築容積獎勵辦法。

職責：
  - 全域常數（平方米換坪、安全梯免計率、樓層欄位）
  - check_bonus_limit() / calc_獎勵率合計()：容積獎勵法規上限驗證
  - calc_容積查核()：逐層 §162 免計查核，回傳 FA/允建/超出/餘量
"""

from core.redcf.law_db import BONUS_都更, BONUS_危老

# ---------------------------------------------------------------------------
# 全域常數（整個 core 引用此處，不各自定義）
# ---------------------------------------------------------------------------
平方米換坪 = 3.3058
安全梯免計率 = 0.15
樓層欄位 = ["啟用", "樓層", "樓板", "計容積", "梯廳", "安全梯", "陽台"]


# ===========================================================================
# 法規查核：容積獎勵上限驗證（P0）
# ===========================================================================
def check_bonus_limit(獎勵拆解: dict, 案件類型: str = "都更") -> list:
    """驗證各項獎勵率是否超出法規上限。回傳超出項目列表，空 list = 全數合規。"""
    items = BONUS_都更 if 案件類型 == "都更" else BONUS_危老
    violations = []
    for item in items:
        val = 獎勵拆解.get(item["key"], 0.0)
        if val > item["cap"] + 1e-6:
            violations.append({
                "項目": item["label"],
                "設定值": f"{val:.1%}",
                "上限": f"{item['cap']:.0%}",
                "超出": f"{val - item['cap']:.1%}",
                "條文": item["article"],
            })
    return violations


def calc_獎勵率合計(獎勵拆解: dict, 案件類型: str = "都更") -> float:
    """各項獎勵率相加，回傳總獎勵率（超出判斷由 check_bonus_limit 負責）。"""
    items = BONUS_都更 if 案件類型 == "都更" else BONUS_危老
    return sum(獎勵拆解.get(item["key"], 0.0) for item in items)


# ===========================================================================
# 容積查核（L2–L4，純函式）
# ===========================================================================
def calc_容積查核(參數: dict, 樓層records: list) -> dict:
    """
    逐層容積查核。樓層records = [{啟用,樓層,樓板,計容積,梯廳,安全梯,陽台}, ...]

    §162（全部逐層）：
      梯廳超出 = Σ 各層 max(0, 梯廳 − 樓板 × 梯廳免計%)
      陽台超出 = Σ 各層 max(0, 陽台 − 樓板 × 陽台免計%)
      陽台1/8投影 = Σ 各層 max(0, 陽台 − 樓板 × 1/8)  ── 另一法則，並列審查
      安全梯   = 允建容積 × 15%（總量上限，踩坑6）
    只計入「啟用」的樓層（取消勾選 = 排除，如 B1F 防空避難室 §117，踩坑5）。
    """
    基地使用面積 = 參數["基地面積"] - 參數["人行廣場"]
    基準容積FA = 基地使用面積 * 參數["容積率"]
    允建容積 = 基準容積FA * (1 + 參數["獎勵率"]) + 參數["容積移轉"]

    梯廳基準 = 參數["梯廳免計基準"] / 100.0
    陽台基準 = 參數["陽台免計基準"] / 100.0

    啟用層 = [f for f in 樓層records if f.get("啟用", True)]

    def 安全取(f, k):
        try:
            return float(f.get(k) or 0)
        except (TypeError, ValueError):
            return 0.0

    梯廳超出 = sum(max(0.0, 安全取(f, "梯廳") - 安全取(f, "樓板") * 梯廳基準) for f in 啟用層)
    陽台超出 = sum(max(0.0, 安全取(f, "陽台") - 安全取(f, "樓板") * 陽台基準) for f in 啟用層)
    陽台1_8超出 = sum(max(0.0, 安全取(f, "陽台") - 安全取(f, "樓板") * 0.125) for f in 啟用層)
    安全梯總量 = sum(安全取(f, "安全梯") for f in 啟用層)
    陽台總量 = sum(安全取(f, "陽台") for f in 啟用層)
    陽台免計面積 = 陽台總量 - 陽台超出

    # 總樓地板面積（含地下室+屋突，全部啟用層）＝營造發包基準（L6 營造成本用；踩坑：非允建坪）
    總樓地板面積 = sum(安全取(f, "樓板") for f in 啟用層)

    安全梯上限 = 允建容積 * 安全梯免計率

    # 計入容積（圖說）：優先採面積表彙總值（圖說為真）；否則由逐層計容積加總
    if 參數.get("面積表計入容積", 0) and 參數["面積表計入容積"] > 0:
        計入容積_圖說 = 參數["面積表計入容積"]
    else:
        計入容積_圖說 = sum(安全取(f, "計容積") for f in 啟用層)

    計入容積_修正後 = 計入容積_圖說 + 梯廳超出 + 陽台超出
    容積餘量 = 允建容積 - 計入容積_修正後

    return {
        "基準容積FA": 基準容積FA, "允建容積": 允建容積,
        "梯廳超出": 梯廳超出, "陽台超出": 陽台超出, "陽台1_8超出": 陽台1_8超出,
        "安全梯上限": 安全梯上限, "安全梯總量": 安全梯總量,
        "陽台總量": 陽台總量, "陽台免計面積": 陽台免計面積,
        "計入容積_圖說": 計入容積_圖說, "計入容積_修正後": 計入容積_修正後,
        "容積餘量": 容積餘量, "啟用層數": len(啟用層),
        "總樓地板面積": 總樓地板面積,
    }
