def calc_總銷(銷售坪數, 住宅單價, 店舖坪數=0.0, 店舖單價=0.0, 車位數=0, 車位單價=0.0) -> dict:
    """總銷 = 住宅 + 店舖 + 車位。住宅坪數 = 銷售坪數 − 店舖坪數（店舖由住宅坪切出）。"""
    住宅坪數 = max(0.0, 銷售坪數 - 店舖坪數)
    住宅銷售 = 住宅坪數 * 住宅單價
    店舖銷售 = 店舖坪數 * 店舖單價
    房地總銷 = 住宅銷售 + 店舖銷售
    車位銷售 = 車位數 * 車位單價
    總銷 = 房地總銷 + 車位銷售
    房地坪 = 住宅坪數 + 店舖坪數
    平均單價 = 房地總銷 / 房地坪 if 房地坪 else 0.0
    return {"住宅坪數": 住宅坪數, "住宅銷售": 住宅銷售, "店舖銷售": 店舖銷售,
            "房地總銷": 房地總銷, "車位銷售": 車位銷售, "總銷": 總銷, "平均單價": 平均單價}


def calc_共同負擔(總銷, 房地總銷, 營造樓地板坪, p: dict, 模式: str = "全案管理") -> dict:
    """
    共同負擔科目（都市更新權利變換實施辦法 — 費用負擔）。三模式：
      全案管理：地主自持土地，土地成本=0（共負比＝地主分攤，實際 ~55%）
      合建　　：建商合作，土地為成本計入（總成本/總銷，實際 ~65%）
      買賣　　：實施者買地開發，土地為成本計入
    校準基準：兩件真實案（合建在建／全案管理審議版；私有校準紀錄）。

    科目：
      A 工程費用 = 營造成本(營造單價×總樓地板坪，all-in) + 設計規劃
      B 管維費用 = A × 管維率
      C 權變費用 = 權變作業 + 拆遷補償 + 租金補償
      D 貸款利息 = 土融(模式) + 建融
      E 稅　　捐 = 營業稅 + 印花稅
      F 管理費用 = 全案管理費 + 代銷費 + 信託/公共基金
      G 土地成本 = 模式≠全案管理 時計入
    ⚠️ 營造基準＝總樓地板面積(含地下室)，非允建坪（踩坑：用允建坪會低估約一半）。
    """
    營造成本 = p["營造單價"] * 營造樓地板坪
    設計規劃 = 營造成本 * p.get("設計規劃率", p.get("設計監造率", 0.014))
    A工程費用 = 營造成本 + 設計規劃

    B管維費用 = A工程費用 * p["管維率"]

    # 權變作業費：僅權利變換（全案管理）模式適用；合建/買賣無權變程序
    權變作業 = 房地總銷 * p["權變作業率"] if 模式 == "全案管理" else 0.0
    # 拆補/租金補償基準＝權變戶（既有地主），非總銷售戶數；預設同戶數，各案覆寫實際權變戶
    權變戶數 = p.get("權變戶數", p["戶數"])
    拆遷補償 = 權變戶數 * p["拆補每戶"]
    租金補償 = 權變戶數 * p["月租金每戶"] * p["安置月數"]
    C權變費用 = 權變作業 + 拆遷補償 + 租金補償

    # D 貸款利息：土融（全案管理土地成本視為 0）+ 建融（以工程費A為基礎）
    土地成本 = 0.0 if 模式 == "全案管理" else p.get("土地成本", 0.0)
    土融利息 = 土地成本 * p["土融成數"] * p["土融利率"] * p["土融年期"]
    建融利息 = A工程費用 * p["建融成數"] * p["建融利率"] * p["建融年期"] * p["現金流係數"]
    D貸款利息 = 土融利息 + 建融利息

    營業稅 = 房地總銷 * p["營業稅率"]
    印花稅 = 總銷 * p["印花稅率"]
    E稅捐 = 營業稅 + 印花稅

    # 全案管理費（實施者服務費）：僅全案管理模式；合建/買賣由建商自理，不列共負
    全案管理費 = 總銷 * p["管理費率"] if 模式 == "全案管理" else 0.0
    代銷費 = 總銷 * p.get("代銷費率", 0.0)
    信託公基金 = 總銷 * p.get("信託費率", 0.0)
    F管理費用 = 全案管理費 + 代銷費 + 信託公基金

    G土地成本 = 土地成本  # 已依模式歸零或取實際值

    共同負擔 = (A工程費用 + B管維費用 + C權變費用 + D貸款利息
               + E稅捐 + F管理費用 + G土地成本)
    return {"A工程費用": A工程費用, "B管維費用": B管維費用, "C權變費用": C權變費用,
            "D貸款利息": D貸款利息, "E稅捐": E稅捐, "F管理費用": F管理費用,
            "G土地成本": G土地成本, "共同負擔": 共同負擔, "模式": 模式,
            "_明細": {"營造成本": 營造成本, "設計規劃": 設計規劃,
                     "權變作業": 權變作業, "拆遷補償": 拆遷補償, "租金補償": 租金補償,
                     "土融利息": 土融利息, "建融利息": 建融利息,
                     "營業稅": 營業稅, "印花稅": 印花稅,
                     "全案管理費": 全案管理費, "代銷費": 代銷費, "信託公基金": 信託公基金}}


def calc_分回(總銷, 共同負擔) -> dict:
    """
    全案管理／權利變換框架（地主分回近全部可建價值扣共負）：
      共同負擔比 = 共同負擔 ÷ 總銷
      地主分回價值 = 總銷 − 共同負擔
      報酬率（利潤/總成本） = 地主分回價值 ÷ 共同負擔
    """
    共負比 = 共同負擔 / 總銷 if 總銷 else 0.0
    地主分回價值 = 總銷 - 共同負擔
    地主分回比 = 地主分回價值 / 總銷 if 總銷 else 0.0
    報酬率 = 地主分回價值 / 共同負擔 if 共同負擔 else 0.0
    return {"共負比": 共負比, "地主分回價值": 地主分回價值,
            "地主分回比": 地主分回比, "報酬率": 報酬率}


def calc_投報全案(銷售坪數, 營造樓地板坪, p: dict, 模式: str = "全案管理") -> dict:
    """串起 總銷 → 共負 → 分回，回傳一張都更全案投報總表（單位：萬元）。
    營造樓地板坪＝總樓地板面積坪（含地下室，來自 calc_容積查核['總樓地板面積']/3.3058）。
    """
    銷 = calc_總銷(銷售坪數, p["住宅單價"], p["店舖坪數"], p["店舖單價"], p["車位數"], p["車位單價"])
    負 = calc_共同負擔(銷["總銷"], 銷["房地總銷"], 營造樓地板坪, p, 模式)
    分 = calc_分回(銷["總銷"], 負["共同負擔"])
    return {**銷, **負, **分}


def calc_投報敏感度(銷售坪數, 營造樓地板坪, p: dict, 模式: str = "全案管理",
                售價變動=(-0.10, -0.05, 0.0, 0.05, 0.10),
                營造變動=(-0.10, -0.05, 0.0, 0.05, 0.10)) -> dict:
    """報酬率敏感度矩陣：列＝營造單價變動、欄＝住宅單價變動（其餘參數固定）。"""
    矩陣 = []
    for d營 in 營造變動:
        列 = []
        for d售 in 售價變動:
            pp = dict(p)
            pp["住宅單價"] = p["住宅單價"] * (1 + d售)
            pp["營造單價"] = p["營造單價"] * (1 + d營)
            列.append(calc_投報全案(銷售坪數, 營造樓地板坪, pp, 模式)["報酬率"])
        矩陣.append(列)
    return {"矩陣": 矩陣, "售價變動": list(售價變動), "營造變動": list(營造變動)}
